export function startupImagePlacement(width, height) {
  const renderedWidth = width * STARTUP_LOCKUP_WIDTH_RATIO;
  const scale = renderedWidth / LOCKUP_WIDTH;
  const renderedHeight = LOCKUP_HEIGHT * scale;
  const x = (width - renderedWidth) / 2;
  const y = (height - renderedHeight) / 2;
  const safeInset = Math.min(width, height) * STARTUP_SAFE_INSET_RATIO;

  if (
    x < safeInset ||
    y < safeInset ||
    width - x - renderedWidth < safeInset ||
    height - y - renderedHeight < safeInset
  ) {
    throw new Error(`Startup lockup falls outside the safe area at ${width}x${height}.`);
  }

  return { x, y, scale, width: renderedWidth, height: renderedHeight, safeInset };
}

function startupImageSvg(width, height) {
  const placement = startupImagePlacement(width, height);
  return svgDocument({
    width,
    height,
    title: 'Hotshot launch screen',
    description: 'The centered Hotshot lockup on a charcoal launch screen.',
    body: `<rect width="${width}" height="${height}" fill="${CHARCOAL}" />
  <g transform="translate(${placement.x.toFixed(6)} ${placement.y.toFixed(6)}) scale(${placement.scale.toFixed(6)})">
    ${lockupGeometry({ background: false })}
  </g>`
  });
}

export function pngDimensions(buffer) {
  const signature = buffer.subarray(0, 8).toString('hex');
  if (signature !== '89504e470d0a1a0a') throw new Error('Rendered output is not a PNG.');
  return { width: buffer.readUInt32BE(16), height: buffer.readUInt32BE(20) };
}

function rgbFromHex(hex) {
  const value = Number.parseInt(hex.slice(1), 16);
  return [(value >> 16) & 255, (value >> 8) & 255, value & 255];
}

function pngFirstPixel(buffer) {
  let offset = 8;
  let bitDepth;
  let colorType;
  const imageData = [];

  while (offset < buffer.length) {
    const length = buffer.readUInt32BE(offset);
    const type = buffer.subarray(offset + 4, offset + 8).toString('ascii');
    const data = buffer.subarray(offset + 8, offset + 8 + length);
    if (type === 'IHDR') {
      bitDepth = data.readUInt8(8);
      colorType = data.readUInt8(9);
    } else if (type === 'IDAT') {
      imageData.push(data);
    } else if (type === 'IEND') {
      break;
    }
    offset += length + 12;
  }

  if (bitDepth !== 8 || (colorType !== 2 && colorType !== 6)) {
    throw new Error(`Unsupported PNG format: bit depth ${bitDepth}, color type ${colorType}.`);
  }

  const scanlines = inflateSync(Buffer.concat(imageData));
  return Array.from(scanlines.subarray(1, 4));
}

async function renderSvg(
  browser,
  svg,
  { width, height, background = null, expectedCornerColor = null }
) {
  const page = await browser.newPage({
    viewport: { width, height },
    deviceScaleFactor: 1
  });
  const dataUrl = `data:image/svg+xml;base64,${Buffer.from(svg).toString('base64')}`;
  const backgroundCss = background ?? 'transparent';

  await page.setContent(`<!doctype html>
<html>
  <head>
    <style>
      html, body { margin: 0; width: ${width}px; height: ${height}px; overflow: hidden; background: ${backgroundCss}; }
      img { display: block; width: ${width}px; height: ${height}px; }
    </style>
  </head>
  <body><img src="${dataUrl}" alt="" /></body>
</html>`);
  await page.locator('img').waitFor({ state: 'visible' });
  const output = await page.screenshot({
    type: 'png',
    omitBackground: background === null
  });
  await page.close();

  if (expectedCornerColor) {
    const actual = pngFirstPixel(output);
    const expected = rgbFromHex(expectedCornerColor);
    if (actual.some((channel, index) => channel !== expected[index])) {
      throw new Error(
        `Expected ${expectedCornerColor} at the PNG corner, received rgb(${actual.join(', ')}).`
      );
    }
  }

  const dimensions = pngDimensions(output);
  if (dimensions.width !== width || dimensions.height !== height) {
    throw new Error(
      `Expected ${width}x${height}, rendered ${dimensions.width}x${dimensions.height}.`
    );
  }
  return output;
}

function buildIco(entries) {
  const headerSize = 6;
  const directorySize = entries.length * 16;
  const header = Buffer.alloc(headerSize + directorySize);
  header.writeUInt16LE(0, 0);
  header.writeUInt16LE(1, 2);
  header.writeUInt16LE(entries.length, 4);

  let offset = header.length;
  entries.forEach(({ size, png }, index) => {
    const entryOffset = headerSize + index * 16;
    header.writeUInt8(size === 256 ? 0 : size, entryOffset);
    header.writeUInt8(size === 256 ? 0 : size, entryOffset + 1);
    header.writeUInt8(0, entryOffset + 2);
    header.writeUInt8(0, entryOffset + 3);
    header.writeUInt16LE(1, entryOffset + 4);
    header.writeUInt16LE(32, entryOffset + 6);
    header.writeUInt32LE(png.length, entryOffset + 8);
    header.writeUInt32LE(offset, entryOffset + 12);
    offset += png.length;
  });

  return Buffer.concat([header, ...entries.map(({ png }) => png)]);
}

async function main() {
  await mkdir(STATIC_DIR, { recursive: true });

  const markSvg = logoMarkSvg();
  const faviconSvg = logoMarkSvg({ compact: true });
  const fullLockupSvg = lockupSvg();
  const safeMaskableSvg = maskableMarkSvg();

  await Promise.all([
    writeFile(path.join(STATIC_DIR, 'logo-mark.svg'), markSvg),
    writeFile(path.join(STATIC_DIR, 'favicon.svg'), faviconSvg),
    writeFile(path.join(STATIC_DIR, 'hotshot-lockup.svg'), fullLockupSvg)
  ]);

  const browser = await chromium.launch({ headless: true });
  try {
    const faviconPngs = new Map();
    for (const size of [16, 32, 48]) {
      const png = await renderSvg(browser, faviconSvg, {
        width: size,
        height: size,
        background: CHARCOAL
      });
      faviconPngs.set(size, png);
      await writeFile(path.join(STATIC_DIR, `favicon-${size}x${size}.png`), png);
    }

    const targets = [
      ['logo-mark.png', markSvg, 512, 512, null],
      ['hotshot-lockup.png', fullLockupSvg, LOCKUP_WIDTH, LOCKUP_HEIGHT, null],
      ['apple-touch-icon.png', markSvg, 180, 180, CHARCOAL],
      ['pwa-192x192.png', markSvg, 192, 192, CHARCOAL],
      ['pwa-512x512.png', markSvg, 512, 512, CHARCOAL],
      ['pwa-1024x1024.png', markSvg, 1024, 1024, CHARCOAL],
      ['maskable-icon-512x512.png', safeMaskableSvg, 512, 512, CHARCOAL]
    ];

    for (const [name, svg, width, height, background] of targets) {
      const png = await renderSvg(browser, svg, { width, height, background });
      await writeFile(path.join(STATIC_DIR, name), png);
    }

    for (const startupImage of IOS_STARTUP_IMAGES) {
      const png = await renderSvg(
        browser,
        startupImageSvg(startupImage.width, startupImage.height),
        {
          width: startupImage.width,
          height: startupImage.height,
          background: CHARCOAL,
          expectedCornerColor: CHARCOAL
        }
      );
      await writeFile(path.join(STATIC_DIR, startupImage.filename), png);
    }

    const ico = buildIco([16, 32, 48].map((size) => ({ size, png: faviconPngs.get(size) })));
    await writeFile(path.join(STATIC_DIR, 'favicon.ico'), ico);
  } finally {
    await browser.close();
  }

  const generated = [
    'logo-mark.svg',
    'hotshot-lockup.svg',
    'favicon.svg',
    'logo-mark.png',
    'hotshot-lockup.png',
    'favicon-16x16.png',
    'favicon-32x32.png',
    'favicon-48x48.png',
    'favicon.ico',
    'apple-touch-icon.png',
    'pwa-192x192.png',
    'pwa-512x512.png',
    'pwa-1024x1024.png',
    'maskable-icon-512x512.png',
    ...IOS_STARTUP_IMAGES.map(({ filename }) => filename)
  ];

  for (const file of generated) {
    await readFile(path.join(STATIC_DIR, file));
  }
  console.log(`Generated ${generated.length} Hotshot brand assets.`);
}

const isMain = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) await main();
