  <section>
    <h2 class="mb-2 text-xl font-semibold tracking-tight">The weekly loop</h2>
    <p class="text-muted-foreground">
      Each week you pick the side you think beats the spread in a handful of NFL games. Lock your
      picks before kickoff, then watch them grade as games finish. Picks you feel better about are
      worth more points. You pick once and it counts in every group you belong to.
    </p>
  </section>

  <section>
    <h2 class="mb-2 text-xl font-semibold tracking-tight">Picking against the spread</h2>
    <p class="text-muted-foreground">
      The spread is the cushion the favorite has to cover. Say the Eagles are favored by 6.5 over
      the Cowboys, written Eagles -6.5. Pick the Eagles and they must win by 7 or more. Pick the
      Cowboys and you win if they lose by 6 or fewer or win outright. You are picking who beats the
      number, not who wins the game.
    </p>
  </section>

  <section>
    <h2 class="mb-2 text-xl font-semibold tracking-tight">Weights</h2>
    <p class="text-muted-foreground">
      Every pick carries a weight you choose. Low is worth 1 point, Medium is worth 3, and High is
      worth 5. Put your weight where you feel strongest. A wrong High pick costs the same 5 points
      it would have earned.
    </p>
  </section>

